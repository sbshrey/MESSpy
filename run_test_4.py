# Currency conversion (approximate rate)
EUR_TO_INR = 90.0

def eur_to_inr(eur_value):
    """Convert Euro value to Indian Rupees"""
    return eur_value * EUR_TO_INR

def format_currency_for_pdf(value, unit="", use_rs_text=True):
    """Format currency value for PDF display, using Rs. instead of ‚Çπ for better compatibility
    
    Note: The ‚Çπ symbol doesn't render properly in PDFs generated by reportlab,
    so we use 'Rs.' text instead for PDF compatibility while keeping ‚Çπ in console output.
    """
    if use_rs_text:
        return f"{value:,.0f} Rs./{unit}"
    else:
        return f"{value:,.0f} ‚Çπ/{unit}"

